package myAPI1Math;

public class MathDemo {
    public static void main(String[] args) {
        //返回绝对值int
        System.out.println(Math.abs(88));
        System.out.println(Math.abs(-88));
        System.out.println();
        //返回比所给数大的double
        System.out.println(Math.ceil(12.35));
        System.out.println(Math.ceil(12.75));
        System.out.println();
        //返回比所给数小的double
        System.out.println(Math.floor(12.35));
        System.out.println(Math.floor(12.75));
        System.out.println();
        //返回两数间最大值int
        System.out.println(Math.max(23,46));
        System.out.println();

        //返回a的b幂次double
        System.out.println(Math.pow(4.2,5.6));
        System.out.println();
        //返回随机数
        System.out.println(Math.random());


    }



}
